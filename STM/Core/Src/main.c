/**
  * @brief Receive value using UART interface
  * @unit RPM
  * @retval uint8_t
  */
void receive_value(bot* m1,bot* m2, bot* m3)
{
	predir0=dir0;
	m1->dir=RxBuffer[0]-48;
	m1->vel=(RxBuffer[1]-48)*100+(RxBuffer[2]-48)*10+(RxBuffer[3]-48)-100;
	m2->dir=RxBuffer[4]-48;
	m2->vel=(RxBuffer[5]-48)*100+(RxBuffer[6]-48)*10+(RxBuffer[7]-48)-100;
	m3->dir=RxBuffer[8]-48;
	m3->vel=(RxBuffer[9]-48)*100+(RxBuffer[10]-48)*10+(RxBuffer[11]-48)-100;
	dir0=RxBuffer[12]-48;
	if (m1->vel>333) m1->vel=0;
	if (m2->vel>333) m2->vel=0;
	if (m3->vel>333) m3->vel=0;
}

/**
  * @brief Transmit velocity value to ROS
  * @unit void
  */

void vel_convert(char* vel_pub)
{
	if (bot1.dir == 0) bot1.cd = '0'; else bot1.cd = '1';
	if (bot2.dir == 0) bot2.cd = '0'; else bot2.cd = '1'; 
	if (bot3.dir == 0) bot3.cd = '0'; else bot3.cd = '1';
	bot1.iw=round(MOTOR1.w+100);
	bot2.iw=round(MOTOR2.w+100);
	bot3.iw=round(MOTOR3.w+100);		
	sprintf(vel_pub, "%c%d%c%d%c%d", bot1.cd, bot1.iw, bot2.cd, bot2.iw, bot3.cd, bot3.iw);
	HAL_UART_Transmit_IT(&huart2, vel_pub, 12);
}

/**
  * @brief Systick Timer Interrupt after 0.01s
  * @unit second
  */
void timeinterrupt(void)
{
  uint32_t wait = 10;

  /* Add a freq to guarantee minimum wait */
  if (wait < HAL_MAX_DELAY)
  {
    wait += (uint32_t)(uwTickFreq);
  }
  if ((HAL_GetTick() - timstart) == wait)
  {
		receive_value(&bot1,&bot2,&bot3);
		if ((dir0==1 && predir0==2) || (dir0==2 && predir0==1))	count=0;	
		if (count<20)
		{
			ballkeeping(dir0); // 2 - len(da bong) , 1- xuong(do bong)
			count++;
		}
		else 	ballkeeping(3);
		encoder_reading(&MOTOR1,&MOTOR2,&MOTOR3);					
		bot1.pulse = PID_motor(&PID_1,bot1.vel,&MOTOR1)*10;
		bot2.pulse = PID_motor(&PID_2,bot2.vel,&MOTOR2)*10;
		bot3.pulse = PID_motor(&PID_3,bot3.vel,&MOTOR3)*10;
		control(bot1.pulse,bot2.pulse,bot3.pulse,bot1.dir,bot2.dir,bot3.dir);
		if (countt>=10) 
			{
				vel_convert(TxBuffer);
				countt=0;
			}
		else countt++;
		timstart = HAL_GetTick();
  }
}
